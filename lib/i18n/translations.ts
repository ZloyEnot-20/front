export type Lang = 'uz' | 'ru' | 'en'

export type TranslationMap = Record<string, string>

function buildMap(ru: Record<string, string>, en: Record<string, string>, uz: Record<string, string>): Record<Lang, TranslationMap> {
  const keys = new Set([...Object.keys(ru), ...Object.keys(en), ...Object.keys(uz)])
  const result: Record<Lang, TranslationMap> = { ru: {}, en: {}, uz: {} }
  for (const k of keys) {
    result.ru[k] = ru[k] ?? en[k] ?? k
    result.en[k] = en[k] ?? ru[k] ?? k
    result.uz[k] = uz[k] ?? ru[k] ?? k
  }
  return result
}

