export const translations = buildMap(ru, en, uz)

export function getT(lang: Lang): (key: keyof typeof ru) => string {
  const map = translations[lang]
  return (key: keyof typeof ru) => map[key as string] ?? translations.ru[key as string] ?? (key as string)
}
